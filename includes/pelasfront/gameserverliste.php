<h2>HLSW benutzen um Gameserver zu finden</h2>

<img src="/gfx_all/hlsw_logo.gif" align="right" vspace="2" hspace="18">
<p align="justify">
F&uuml;r nahezu alle g&auml;ngigen Spiele hilft euch das Programm HLSW um
laufende Server ausfindig zu machen. HLSW l&auml;dt sich die aktuelle Liste
automatisch herunter. <b>Wichtig:</b> Ihr ben&ouml;tigt die aktuellste Version HLSW.</p>

<p align="justify"><b>Wichtig:</b> Bitte stellt alle nicht benutzten Netzwerkkarten aus, auch die Firewire-Interfaces,
da es sonst passieren kann, dass ihr keine Server findet. Gleiches gilt f&uuml;r Firewalls, denn beides
verursacht Probleme im Bezug auf Broadcasts.</p>

